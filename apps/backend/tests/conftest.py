import pytest
from fastapi.testclient import TestClient
from sqlalchemy import create_engine
from sqlalchemy.orm import sessionmaker
from sqlalchemy.pool import StaticPool

from app.database import Base, get_db
from app.main import app

# We use an in-memory SQLite database for testing the core API.
# Note: Since pgvector is specific to Postgres, we mock out vector-dependent operations if they run,
# or we use this SQLite setup for general routes like health and structure verification.
SQLALCHEMY_DATABASE_URL = "sqlite://"

engine = create_engine(
    SQLALCHEMY_DATABASE_URL,
    connect_args={"check_same_thread": False},
    poolclass=StaticPool,
)
TestingSessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)


@pytest.fixture(scope="function")
def db():
    # In SQLite, Vector is treated as an unknown type.
    # To prevent errors during table creation in tests, we can dynamically mock the vector type
    # if it's imported or registered, or just let SQLite create tables where vector column has a mocked type.
    # To keep it extremely robust and simple:
    Base.metadata.create_all(bind=engine)
    db_session = TestingSessionLocal()
    try:
        yield db_session
    finally:
        db_session.close()
        Base.metadata.drop_all(bind=engine)


